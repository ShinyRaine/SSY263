Robot2WCtrl::Robot2WCtrl(/* args */)
  : Node("robot_2w_control"), K_(Eigen::Matrix3d::Zero()), first_message_(false), init_ctrl_(true), robot_2w_odom_msg_(std::make_shared<nav_msgs::msg::Odometry>()), got_odom_(false), trajectory_point_msg_(std::make_shared<geometry_msgs::msg::Point>())
{

  RCLCPP_WARN_STREAM(get_logger(),"pose: "<< robot_2w_odom_msg_->pose.pose.position.x<<", "<< robot_2w_odom_msg_->pose.pose.orientation.w);

  RCLCPP_WARN_STREAM(get_logger(),"pose: "<< trajectory_point_msg_->x<<", "<< trajectory_point_msg_->y);

  // Init Parameters. Load node parameters
  init();

  // Set parameter callback function
  callback_handle_ = this->add_on_set_parameters_callback(std::bind(&Robot2WCtrl::parametersCallback, this, _1));

  // Create subscriber to receive the robot_2w odometry. This state is published by gazebo.
  sub_odometry_ = create_subscription<nav_msgs::msg::Odometry>(odom_topic_name_, 10,
                                                               std::bind(&Robot2WCtrl::odom_topic_callback, this, _1));

  // Create subscriber to receive the target position generated by the trajectory generator.
  // The target position is wrt the odom frame 
  sub_trajectory_ = create_subscription<geometry_msgs::msg::Point>(
      trajectory_topic_name_, 10, std::bind(&Robot2WCtrl::trajectory_topic_callback, this, _1));

  // Create a publisher to send the commanded robot velocity to the differential drive
  // the cmd_vel is wrt base_footprint frame. It is velocity vector in the robot's frame!
  robot_twist_pub_ = create_publisher<geometry_msgs::msg::Twist>(commanded_twist_topic_name_, 10);

  // Create a publisher to plot the pose errors wrt robot frame
  pose_error_pub_ = create_publisher<geometry_msgs::msg::Point>(pose_error_topic_name_, 10);

  // Create a publisher to plot the robot's pose wrt odom frame
  pose_pub_ = create_publisher<geometry_msgs::msg::Point>(pose_topic_name_, 10);

  // MAIN control thread. Based on the robot's odom position and the target position, this main thread
  // will compute the cmd_vel wrt robot's frame (base_footprint). This cmd_vel is needed for the 
  // differential drive to compute the wheel velocities and move the robot
  timer_ =
      this->create_wall_timer(std::chrono::milliseconds(ctrl_period_), std::bind(&Robot2WCtrl::timer_callback, this));
}

