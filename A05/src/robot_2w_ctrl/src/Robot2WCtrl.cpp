void Robot2WCtrl::init()
{
  // ROS parameters
  // Declare the ros parameters

  declare_parameter("odom_topic_name", "/odom");
  declare_parameter("trajectory_topic_name", "/path_generator/trajectory");
  declare_parameter("commanded_twist_topic_name", "/cmd_vel");
  declare_parameter("ctrl_gains", std::vector<double>{ 1.0, 1.0, 0.1 });
  declare_parameter("ctrl_period", 1);
  declare_parameter("publish_pose_error", true);
  declare_parameter("pose_error_topic_name", "/pose_error");
  declare_parameter("pose_topic_name", "/pose");

  // Get the ros parameters (for example, from a yaml file or via the console )
  load_parameters();

  RCLCPP_INFO_STREAM(get_logger(), "K:  \n" << K_);
}

rcl_interfaces::msg::SetParametersResult
Robot2WCtrl::parametersCallback(const std::vector<rclcpp::Parameter>& parameters)
{
  // Each time we change the parameters using the terminal, e.g. ros2 param set ...
  // this function will be called, and the input argument "parameters" is populated
  // with the modified parameter. Depending on the name of the parameter, we need
  // to update its corresponding member variable

  rcl_interfaces::msg::SetParametersResult result;
  result.successful = true;
  result.reason = "success";

  RCLCPP_WARN_STREAM(get_logger(), "New parameters [" << parameters.size() << "]");

  for (const auto& param : parameters)
  {
    RCLCPP_INFO_STREAM(get_logger(), param.get_name()
                                         << "<" << param.get_type_name() << ">: " << param.value_to_string());

    if ("odom_topic_name" == param.get_name())
    {
      odom_topic_name_ = param.as_string();
      RCLCPP_INFO_STREAM(get_logger(), "odom_topic_name_: " << odom_topic_name_);
    }

    if ("trajectory_topic_name" == param.get_name())
    {
      trajectory_topic_name_ = param.as_string();
      RCLCPP_INFO_STREAM(get_logger(), "trajectory_topic_name_: " << trajectory_topic_name_);
    }

    if ("commanded_twist_topic_name" == param.get_name())
    {
      commanded_twist_topic_name_ = param.as_string();
      RCLCPP_INFO_STREAM(get_logger(), "commanded_twist_topic_name_: " << commanded_twist_topic_name_);
    }

    if ("ctrl_gains" == param.get_name())
    {
      std::vector<double> k = param.as_double_array();
      // Populate the gain Matrix
      for (size_t i = 0; i < 3; i++)
      {
        K_(i, i) = k.at(i);
      }

      RCLCPP_INFO_STREAM(get_logger(), "K_: \n" << K_.matrix());
    }

    if ("ctrl_period" == param.get_name())
    {
      ctrl_period_ = param.as_int();
      RCLCPP_INFO_STREAM(get_logger(), "ctrl_period_: " << ctrl_period_);
    }

    if ("publish_pose_error" == param.get_name())
    {
      publish_pose_error_ = param.as_bool();
      RCLCPP_INFO_STREAM(get_logger(), "publish_pose_error_: " << publish_pose_error_);
    }
  }

  return result;
}

bool Robot2WCtrl::load_parameters()
{
  // Get the ros parameters (for example, from a yaml file or via the console )
  odom_topic_name_ = get_parameter("odom_topic_name").as_string();
  trajectory_topic_name_ = get_parameter("trajectory_topic_name").as_string();
  commanded_twist_topic_name_ = get_parameter("commanded_twist_topic_name").as_string();
  std::vector<double> k = get_parameter("ctrl_gains").as_double_array();
  ctrl_period_ = get_parameter("ctrl_period").as_int();
  publish_pose_error_ = get_parameter("publish_pose_error").as_bool();
  pose_error_topic_name_ = get_parameter("pose_error_topic_name").as_string();
  pose_topic_name_ = get_parameter("pose_topic_name").as_string();

  // Populate the gain Matrix
  for (size_t i = 0; i < 3; i++)
  {
    K_(i, i) = k.at(i);
  }

  // RCLCPP_WARN_STREAM(get_logger(), "K: \n" << K_.matrix());

  return true;
}

